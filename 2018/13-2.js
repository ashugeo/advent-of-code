let lines = data.split('\n');

lines.pop();

let map = [];

for (let line of lines) {
    map.push(line.split(''));
}

let carts = [];

const turns = ['left', 'straight', 'right'];
const dirs = ['^', '>', 'v', '<'];

for (let row = 0; row < map.length; row += 1) {
    for (let col = 0; col < map[row].length; col += 1) {
        const cell = map[row][col];

        const arrows = ['>', 'v', '<', '^'];
        if (arrows.indexOf(cell) > -1) {
            carts.push({dir: cell, x: col, y: row, turn: turns[0]});
            if (cell === '>' || cell === '<') {
                map[row][col] = '-';
            } else if (cell === '^' || cell === 'v') {
                map[row][col] = '|';
            }
        }
    }
}

function setup() {
    createCanvas(window.innerWidth, window.innerHeight);
    background(0);
    fill(255);
    stroke(255);
    // frameRate(1);
}

function draw() {
    // scale(10);
    stroke('white');
    background(0);
    // for (let y = 0; y < map.length; y += 1) {
    //     for (let x = 0; x < map[y].length; x += 1) {
    //         const cell = map[y][x];
    //
    //         if (cell && cell !== ' ') {
    //             if (cell === '+') stroke(200);
    //             else stroke(255);
    //             point(x, y);
    //         }
    //     }
    // }

    stroke('red');
    for (let cart of carts) {
        point(cart.x, cart.y);
    }

    for (let cart of carts) {
        const cell = map[cart.y][cart.x];

        if (cell === '|') {
            if (cart.dir === '^') {
                cart.y = cart.y - 1;
            } else if (cart.dir === 'v') {
                cart.y = cart.y + 1;
            }
        } else if (cell === '-') {
            if (cart.dir === '<') {
                cart.x = cart.x - 1;
            } else if (cart.dir === '>') {
                cart.x = cart.x + 1;
            }
        } else if (cell === '\\') {
            if (cart.dir === '>') {
                cart.dir = 'v';
                cart.y = cart.y + 1;
            } else if (cart.dir === 'v') {
                cart.dir = '>';
                cart.x = cart.x + 1;
            } else if (cart.dir === '<') {
                cart.dir = '^';
                cart.y = cart.y - 1;
            } else if (cart.dir === '^') {
                cart.dir = '<';
                cart.x = cart.x - 1;
            }
        } else if (cell === '/') {
            if (cart.dir === '>') {
                cart.dir = '^';
                cart.y = cart.y - 1;
            } else if (cart.dir === 'v') {
                cart.dir = '<';
                cart.x = cart.x - 1;
            } else if (cart.dir === '<') {
                cart.dir = 'v';
                cart.y = cart.y + 1;
            } else if (cart.dir === '^') {
                cart.dir = '>';
                cart.x = cart.x + 1;
            }
        } else if (cell === '+') {
            if (cart.turn === 'left') {
                cart.dir = dirs[(dirs.indexOf(cart.dir) - 1 + dirs.length) % dirs.length];
            } else if (cart.turn === 'right') {
                cart.dir = dirs[(dirs.indexOf(cart.dir) + 1) % dirs.length];
            }

            if (cart.dir === '^') {
                cart.y = cart.y - 1;
            } else if (cart.dir === 'v') {
                cart.y = cart.y + 1;
            } else if (cart.dir === '<') {
                cart.x = cart.x - 1;
            } else if (cart.dir === '>') {
                cart.x = cart.x + 1;
            }

            cart.turn = turns[(turns.indexOf(cart.turn) + 1) % turns.length];
            // if (carts.indexOf(cart) === 0) console.log(cart.turn);
        }

        for (let cart of carts) {
            cartsHere = carts.filter(c => c.x === cart.x && c.y === cart.y);
            if (cartsHere.length > 1) {
                carts = carts.filter(c => !(c.x === cartsHere[0].x && c.y === cartsHere[0].y));
                break;
            }
        }
    }

    stroke('red');

    // console.log(carts.length);

    if (carts.length === 1) {
        console.log('last cart @', carts[0].x, carts[0].y);
        noLoop();
    }
}

